								<p>TIMESINDONESIA, NGAWI </p>
								<p>Gunung Lawu sudah masyhur menjadi jujugan para pecinta kegiatan pendakian. Para pendaki umumnya memilih lewat jalur pendakian di Kabupaten Magetan, atau di Karanganyar, Jawa Tengah untuk mencapai puncak. Namun tahukah, di Kabupaten Ngawi juga terdapat jalur pendakian Gunung Lawu. Jalur pendakian Gunung Lawu via Ngawi dijuluki dengan sebutan Wukir Bayi. Pos pemberangkatan ada di Desa Kletekan, Kecamatan Jogorogo, masuk kawasan hutan perhutani. Tidak sedikit juga yang menyebutnya jalur pendakian Gunung Lawu via Jogorogo. Ketua Karang Taruna desa setempat, Rolanda Devi Cahyana mengatakan, jalur pendakian Wukir Bayi termasuk jalur pendakian Gunung Lawu yang termasuk tua. Jalur ini sering digunakan untuk laku tirakat.</p>
								<p>"Bisa dikatakan jalur pendakian gunung Lawu via Jogorogo ini termasuk tua, jalur untuk tirakat," kata Yana kepada TIMES Indonesia, Selasa (27/9/2022).</p>
								<p>Khusus untuk laku tirakat, Yana Menjelaskan, masyarakat sekitar memiliki kebiasaan untuk mendaki gunung Lawu setiap tahun sekali. Pelaksanaannya saban bulan Suro. Masyarakat Kletekan biasa mendaki saat malam Jumat Legi, pada bulan tersebut.</p>
								<p>"Memang dipakai masyarakat Jawa untuk jalur tirakat. Tidak hanya masyarakat sekitar, tapi juga berbagai wilayah. Setiap malam Jumat Legi pada bulan Suro juga rutin dilakukan upacara Wedhus Kendhit," kata Yana.</p>
								<p>Yana mengatakan, jalur pendakian via Jogorogo dipercaya sebagai pintu masuk gunung Lawu. Hal itu juga dipercaya para pendaki yang memang memiliki hajat laku tirakat dengan naik gunung Lawu via jalur Jogorogo.</p>
								<p>"Masyarakat percaya, pintu masuk gunung Lawu ada di jalur pendakian via Jogorogo ini," kata Yana.</p>
								<p>Meskipun dikenal sebagai jalur pendakian gunung Lawu khusus untuk tirakat, namun tidak banyak pantangan yang neko-neko. Hanya saja, para pendaki diwanti-wanti untuk selalu menjaga omongan, dan tidak boleh congkak selama mendaki.</P>
								<p>"Juga ada tradisi, setiap pendaki yang akan naik via jalur ini untuk membawa sepasang kupat lepet. Dua jenis makanan ini dibawa selama naik dan turun gunung Lawu," kata Yana.</p>
